#include "Skill.h"

/*****************************************************************************/
Skill::Skill()
{
	id = 0;
	name = "";
	type = 0;
}
/*****************************************************************************/
Skill::Skill(int num, QString nom, int genre)
{
	id = num;
	name = nom;
	type = genre;
}
/*****************************************************************************/
int Skill::getId()
{
	return id;
}
/*****************************************************************************/
QString Skill::getName()
{
	return name;
}
/*****************************************************************************/
int Skill::getType()
{
	return type;
}
