        FantasyBowl est la transposition en jeu vidéo du formidable jeu de plateau BloodBowl© de Games Workshop. 
        Cette version utilise la librairie Qt et la version finale sera disponible pour tous les systèmes d'exploitations 
        supportant cette librairie.
        <br />
        <br />
        Toutes les références, les logos, les images, les règles et autres ressources appartenant à Games Workshop ont été 
        honteusement emprunté sans l'autorisation de l'éditeur. Mais bon, ils vont pas s'en plaindre ça leur fait de la pub. 
        Eh puis j'ai le jeu en boîte alors hein, avec tout l'argent que j'ai dépensé dans les figurines ils peuvent bien 
        me filer 3 images.
		<br />
        <br />
		Reste que les p'tits gars de GamesWorkshop font du superbe travail, les documents ont toujours 
		été de très bonne qualité et les figurines sont bigrement bien foutues. C'est un réel plaisir de 
		les peindre, enfin quand on a le temps.
